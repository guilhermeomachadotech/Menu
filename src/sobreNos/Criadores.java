package sobreNos;

public class Criadores {
	private String nomeCriador;
	private int idadeCriador;
	private String cargoCriador;
	private String descCriador;
	public String getNomeCriador() {
		return nomeCriador;
	}
	public void setNomeCriador(String nomeCriador) {
		this.nomeCriador = nomeCriador;
	}
	public int getIdadeCriador() {
		return idadeCriador;
	}
	public void setIdadeCriador(int idadeCriador) {
		this.idadeCriador = idadeCriador;
	}
	public String getCargoCriador() {
		return cargoCriador;
	}
	public void setCargoCriador(String cargoCriador) {
		this.cargoCriador = cargoCriador;
	}
	public String getDescCriador() {
		return descCriador;
	}
	public void setDescCriador(String descCriador) {
		this.descCriador = descCriador;
	}
	
}
